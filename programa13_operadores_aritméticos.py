#!/usr/bin/python
#Utilizando operadores aritméticos

print(5+2)		#Utilizando o operador de SOMA
print(5-2)		#Utilizando o operador de SUBTRAÇÃO
print(5*2)		#Utilizando o operador de MULTIPLICAÇÃO
print(5/2)		#Utilizando o operador de DIVISÃO S/ ARREDONDAMENTO
print(5//2)		#Utilizando o operador de DIVISÃO C/ ARREDONDAMENTO
print(5%2)		#Utilizando o operador de MÓDULO
print(5**2)		#Utilizando o operador de EXPONENCIAÇÃO
