#!/usr/bin/python
# Verifica se 3 e 4 são iguais
print(3==4) 
print(bool(3==4))
# Verifica se 3 e 4 são diferentes 
print(3!=4) 
print(bool(3!=4))
# Verifica se 3 é maior que 4
print(3>4) 
print(bool(3>4))
# Verifica se 3 é menor que 4
print(3<4) 
print(bool(3<4))
# Verifica se 3 é maior ou igual a 4
print(3>=4)
print(bool(3>=4)) 
# Verifica se 3 é menor ou igual a 4
print(3<=4)
print(bool(3<=4))
